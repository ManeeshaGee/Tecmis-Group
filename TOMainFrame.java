       public void uploadFile(File file,String medical_number) {
        try {
                    FileInputStream fis = new FileInputStream(file);
                    

                    PreparedStatement statement = conn.prepareStatement("UPDATE `medical_details`SET `document`? =  WHERE `id` = '?';");
                    statement.setBinaryStream(1, fis, (int) file.length());
                    statement.setString(2, medical_number);
                    
                    statement.executeUpdate();

                    JOptionPane.showMessageDialog(this, "PDF file uploaded successfully.");

                    fis.close();
                    statement.close();
                } catch (Exception ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Error uploading PDF file to database: " + ex.getMessage());
                }
    }

