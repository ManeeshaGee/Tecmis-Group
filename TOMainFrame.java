    private void medical_data_search1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_medical_data_search1MouseClicked
        
        String search_medical_data_id = search_medical_data_id1.getText();

try {
    Statement stmt = conn.createStatement();
    String sql = "SELECT * FROM medical_details WHERE id = '"+ search_medical_data_id +"'";
    rs = stmt.executeQuery(sql);

    if (rs.next()) {
        // Retrieve data from the result set
        String reg_no = rs.getString("reg_no");
        String course_code = rs.getString("course_code");
        String date = rs.getString("date");
        int hours = rs.getInt("hours");
        String status = rs.getString("status");

        // Display the retrieved data in JLabels
        m_std_reg_no1.setText(reg_no);
        m_course_code1.setText(course_code);
        m_lec_time_per_sess1.setText(date);
        m_lec_date1.setText(String.valueOf(hours));
        m_status2.setSelectedItem(status);
        
        JOptionPane.showMessageDialog(null, "Data Retrieved Successfully");

    } else {
        JOptionPane.showMessageDialog(null, "No record found for the given ID", "Record Not Found", JOptionPane.WARNING_MESSAGE);
    }

} catch (SQLException e) {
    // Handle any SQL exceptions that may occur
    JOptionPane.showMessageDialog(null, "An error occurred while retrieving data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    System.err.println("SQL Exception: " + e.getMessage());
}
        
    }//GEN-LAST:event_medical_data_search1MouseClicked

